x = 0 # Przypisanie wartości 0 do zmiennej x

if x > 0: # instrukcja if wskazująca, ze mamy do czeynienia z warunkiem logicznym, który zdefiniowany jest za słowem if
    print('x większe od zera') # kod do wykonania, jeśli warunek logiczny jest spełniony
else:
    print('x nie jest większe od zera') # kod do wykonania, jeśli warunek logiczny nie jest spełniony

print('koniec')

